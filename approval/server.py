# Jira ticket integration
# ---------------------------------------------------------------------------

def _jira_auth() -> tuple[str, str]:
    return (JIRA_USER, JIRA_API_TOKEN)


def _create_jira_ticket(intent: dict) -> str | None:
    if not JIRA_URL or not JIRA_API_TOKEN:
        return None

    cluster = intent.get("cluster", "unknown")
    ns = intent.get("namespace", "")
    wl = intent.get("workload", "")
    saving = intent.get("monthly_saving_estimate", 0)
    cur = intent.get("current", {})
    rec = intent.get("recommended", {})

    summary = f"Right-size {wl} on {cluster} (save ${saving:.2f}/mo)"
    description = (
        f"*Cluster:* {cluster}\n"
        f"*Namespace:* {ns}\n"
        f"*Workload:* {wl}\n"
        f"*Current:* CPU {cur.get('cpu_request')}, Memory {cur.get('memory_request')}\n"
        f"*Recommended:* CPU {rec.get('cpu_request')}, Memory {rec.get('memory_request')}\n"
        f"*Estimated Saving:* ${saving:.2f}/mo\n\n"
        f"Generated by FinOps Autonomous Loop."
    )

    try:
        resp = httpx.post(
            f"{JIRA_URL}/rest/api/3/issue",
            auth=_jira_auth(),
            json={
                "fields": {
                    "project": {"key": JIRA_PROJECT_KEY},
                    "summary": summary,
                    "description": {
                        "version": 1,
                        "type": "doc",
                        "content": [{"type": "paragraph", "content": [{"type": "text", "text": description}]}],
                    },
                    "issuetype": {"name": "Task"},
                }
            },
            timeout=15,
        )
        resp.raise_for_status()
        key = resp.json().get("key", "")
        log.info("Created Jira ticket %s for %s/%s", key, ns, wl)
        return key
    except Exception as e:
        log.warning("Jira ticket creation failed: %s", e)
        return None


def _comment_jira_ticket(ticket_key: str, comment: str) -> None:
    if not ticket_key or not JIRA_URL:
        return
    try:
        httpx.post(
            f"{JIRA_URL}/rest/api/3/issue/{ticket_key}/comment",
            auth=_jira_auth(),
            json={"body": {"version": 1, "type": "doc", "content": [
                {"type": "paragraph", "content": [{"type": "text", "text": comment}]}
            ]}},
            timeout=15,
        )
        log.info("Updated Jira ticket %s", ticket_key)
    except Exception as e:
        log.warning("Jira comment failed: %s", e)


def _close_jira_ticket(ticket_key: str) -> None:
    if not ticket_key or not JIRA_URL:
        return
    try:
        resp = httpx.get(
            f"{JIRA_URL}/rest/api/3/issue/{ticket_key}/transitions",
            auth=_jira_auth(), timeout=15,
        )
        resp.raise_for_status()
        for t in resp.json().get("transitions", []):
            if t["name"].lower() in ("done", "closed", "resolve", "resolved"):
                httpx.post(
                    f"{JIRA_URL}/rest/api/3/issue/{ticket_key}/transitions",
                    auth=_jira_auth(),
                    json={"transition": {"id": t["id"]}},
                    timeout=15,
                )
                log.info("Closed Jira ticket %s", ticket_key)
                return
    except Exception as e:
        log.warning("Jira close failed: %s", e)


