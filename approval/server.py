@flask_app.route("/slack/events", methods=["POST"])
def slack_events():
    return bolt_handler.handle(request)


@flask_app.route("/intent", methods=["POST"])
def receive_intent():
    body = request.get_json(force=True)

    if body.get("decision") != "approve":
        return jsonify({"status": f"Intent blocked by Observer: {body.get('reasons')}"}), 200

    key = _proposal_key(body)
    if key in processed:
        return jsonify({"status": f"Already processed: {key}"}), 200

    ticket_key = _create_jira_ticket(body)
    if ticket_key:
        body["jira_ticket"] = ticket_key
        body["jira_url"] = JIRA_URL

    pending_intents[key] = body
    _forward_to_aap_event_stream(body)
    log.info("Received intent: %s (ticket: %s)", key, ticket_key or "none")
    return jsonify({"status": f"Intent received: {key}", "jira_ticket": ticket_key}), 200


@flask_app.route("/health", methods=["GET"])
def health():
    return "ok", 200


