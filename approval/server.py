# AAP API integration
# ---------------------------------------------------------------------------

def _aap_headers() -> dict:
    return {"Authorization": f"Bearer {AAP_API_TOKEN}", "Content-Type": "application/json"}


def _find_pending_approval(workload: str, namespace: str) -> int | None:
    if not AAP_API_URL or not AAP_API_TOKEN:
        return None

    try:
        resp = httpx.get(
            f"{AAP_API_URL}/v2/workflow_approvals/?status=pending",
            headers=_aap_headers(), verify=False, timeout=15,
        )
        resp.raise_for_status()

        for approval in resp.json().get("results", []):
            source_wf = approval.get("summary_fields", {}).get("source_workflow_job", {})
            wf_job_id = source_wf.get("id")
            if not wf_job_id:
                continue

            job_resp = httpx.get(
                f"{AAP_API_URL}/v2/workflow_jobs/{wf_job_id}/",
                headers=_aap_headers(), verify=False, timeout=15,
            )
            job_resp.raise_for_status()
            extra_vars = job_resp.json().get("extra_vars", "{}")
            if isinstance(extra_vars, str):
                extra_vars = json.loads(extra_vars)

            if extra_vars.get("workload") == workload and extra_vars.get("namespace") == namespace:
                log.info("Found pending approval %d for %s/%s", approval["id"], namespace, workload)
                return approval["id"]

    except Exception as e:
        log.warning("AAP approval lookup failed: %s", e)

    return None


def _approve_aap_workflow(approval_id: int) -> tuple[int, str]:
    try:
        resp = httpx.post(
            f"{AAP_API_URL}/v2/workflow_approvals/{approval_id}/approve/",
            headers=_aap_headers(), verify=False, timeout=15,
        )
        resp.raise_for_status()
        log.info("AAP approval %d approved", approval_id)
        return 200, f"AAP workflow approved. Remediation running via AAP."
    except Exception as e:
        log.warning("AAP approve failed: %s", e)
        return 500, f"AAP approve failed: {e}"


def _deny_aap_workflow(approval_id: int) -> tuple[int, str]:
    try:
        resp = httpx.post(
            f"{AAP_API_URL}/v2/workflow_approvals/{approval_id}/deny/",
            headers=_aap_headers(), verify=False, timeout=15,
        )
        resp.raise_for_status()
        log.info("AAP approval %d denied", approval_id)
        return 200, f"AAP workflow denied."
    except Exception as e:
        log.warning("AAP deny failed: %s", e)
        return 500, f"AAP deny failed: {e}"


def _forward_to_aap_event_stream(intent: dict) -> None:
    if not AAP_EVENT_STREAM_URL:
        return
    auth = (AAP_EVENT_STREAM_USER, AAP_EVENT_STREAM_PASS) if AAP_EVENT_STREAM_USER else None
    try:
        httpx.post(AAP_EVENT_STREAM_URL, json=intent, auth=auth, verify=False, timeout=15)
        log.info("Forwarded intent to AAP Event Stream")
    except Exception as e:
        log.warning("AAP Event Stream POST failed: %s", e)


# ---------------------------------------------------------------------------
# K8s helpers — deny-path suppression + Actor Job creation (fallback)
# ---------------------------------------------------------------------------

def _get_k8s_apps_client(cluster: str):
    import kubernetes.client
    import kubernetes.config

    safe = _sanitize_env_name(cluster)
    host = os.environ.get(f"K8S_AUTH_HOST_{safe}", os.environ.get("K8S_AUTH_HOST", ""))
    token = os.environ.get(f"K8S_AUTH_API_KEY_{safe}", os.environ.get("K8S_AUTH_API_KEY", ""))

    if host and token:
        conf = kubernetes.client.Configuration()
        conf.host = host
        conf.verify_ssl = False
        api_client = kubernetes.client.ApiClient(conf)
        api_client.set_default_header("Authorization", f"Bearer {token}")
        return kubernetes.client.AppsV1Api(api_client)

    try:
        kubernetes.config.load_incluster_config()
        return kubernetes.client.AppsV1Api()
    except Exception:
        return None


def _suppress_workload(cluster: str, namespace: str, workload: str) -> bool:
    apps_api = _get_k8s_apps_client(cluster)
    if not apps_api:
        log.warning("No K8s client for cluster '%s' — suppression skipped", cluster)
        return False

    suppress_until = (datetime.now(timezone.utc) + timedelta(days=7)).strftime("%Y-%m-%dT%H:%M:%SZ")
    try:
        apps_api.patch_namespaced_deployment(
            name=workload, namespace=namespace,
            body={"metadata": {"annotations": {
                "finops.redhat.com/suppressed-until": suppress_until,
                "finops.redhat.com/denied-by": "human",
                "finops.redhat.com/denied-at": datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
            }}},
        )
        log.info("Suppressed %s/%s until %s", namespace, workload, suppress_until)
        return True
    except Exception as e:
        log.warning("Failed to annotate %s/%s: %s", namespace, workload, e)
        return False


def _create_actor_job(intent: dict) -> tuple[int, str]:
    import kubernetes.client
    import kubernetes.config

    cluster = intent.get("cluster", "")
    ns = intent.get("namespace", "")
    wl = intent.get("workload", "")
    container = intent.get("container", "")
    cur = intent.get("current", {})
    rec = intent.get("recommended", {})
    saving = intent.get("monthly_saving_estimate", 0)

    job_name = f"finops-actor-{wl}-{datetime.now(timezone.utc).strftime('%Y%m%d%H%M%S')}"[:63]

    try:
        kubernetes.config.load_incluster_config()
        batch_api = kubernetes.client.BatchV1Api()
    except Exception as e:
        return 500, f"Cannot create Job (not in-cluster?): {e}"

    extra_vars = json.dumps({
        "cluster": cluster, "namespace": ns, "workload": wl, "container": container,
        "cpu_req": rec.get("cpu_request"), "mem_req": rec.get("memory_request"),
        "expected_cpu": cur.get("cpu_request"), "expected_mem": cur.get("memory_request"),
        "saving": str(saving), "slack_webhook_url": SLACK_WEBHOOK_URL,
    })

    job = kubernetes.client.V1Job(
        api_version="batch/v1", kind="Job",
        metadata=kubernetes.client.V1ObjectMeta(
            name=job_name, namespace=ACTOR_JOB_NAMESPACE,
            labels={"app": "finops-actor", "finops.redhat.com/workload": wl},
        ),
        spec=kubernetes.client.V1JobSpec(
            backoff_limit=0, ttl_seconds_after_finished=3600,
            template=kubernetes.client.V1PodTemplateSpec(
                spec=kubernetes.client.V1PodSpec(
                    service_account_name="finops-actor", restart_policy="Never",
                    containers=[kubernetes.client.V1Container(
                        name="actor", image=ACTOR_IMAGE,
                        command=["ansible-playbook", "playbooks/remediate-safe.yml", "-e", extra_vars],
                        env_from=[kubernetes.client.V1EnvFromSource(
                            secret_ref=kubernetes.client.V1SecretEnvSource(name="finops-cluster-tokens"))],
                        env=[kubernetes.client.V1EnvVar(name="SLACK_WEBHOOK_URL", value=SLACK_WEBHOOK_URL)],
                    )],
                ),
            ),
        ),
    )

    try:
        batch_api.create_namespaced_job(namespace=ACTOR_JOB_NAMESPACE, body=job)
        log.info("Created Actor Job '%s' for %s/%s", job_name, ns, wl)
        return 200, f"Approved: {ns}/{wl}. Actor Job '{job_name}' created."
    except Exception as e:
        log.warning("Failed to create Actor Job: %s", e)
        return 500, f"Failed to create Actor Job: {e}"


# ---------------------------------------------------------------------------
# Actions — approve / deny via AAP or fallback
# ---------------------------------------------------------------------------

def _handle_approve(cluster: str, namespace: str, workload: str) -> tuple[int, str]:
    key = f"{cluster}__{namespace}__{workload}"
    if key in processed:
        return 200, "Already processed."

    processed.add(key)

    if AAP_API_URL:
        approval_id = _find_pending_approval(workload, namespace)
        if approval_id:
            return _approve_aap_workflow(approval_id)
        log.warning("No pending AAP approval for %s/%s — falling back to direct Job", namespace, workload)

    intent = pending_intents.get(key)
    if intent:
        return _create_actor_job(intent)

    return 500, f"No pending intent found for {namespace}/{workload} and no AAP approval available."


def _handle_deny(cluster: str, namespace: str, workload: str) -> str:
    key = f"{cluster}__{namespace}__{workload}"
    if key in processed:
        return "Already processed."

    processed.add(key)

    if AAP_API_URL:
        approval_id = _find_pending_approval(workload, namespace)
        if approval_id:
            _deny_aap_workflow(approval_id)

    suppressed = _suppress_workload(cluster, namespace, workload)
    suffix = " Suppressed for 7 days." if suppressed else ""
    return f"Denied: {namespace}/{workload}.{suffix}"


# ---------------------------------------------------------------------------
# Slack Bolt action handlers
# ---------------------------------------------------------------------------

@bolt_app.action("finops_approve")
def handle_approve(ack, body, respond):
    ack()
    key = body["actions"][0]["value"]
    cluster, namespace, workload = _parse_proposal_key(key)

    if not namespace or not workload:
        respond(text=f"Invalid proposal key: {key}", replace_original=True)
        return

    log.info("Slack approve clicked: %s", key)
    intent = pending_intents.get(key, {})
    ticket_key = intent.get("jira_ticket", "")
    _comment_jira_ticket(ticket_key, f"Approved via Slack. Remediation triggered.")

    status, msg = _handle_approve(cluster, namespace, workload)
    pending_intents.pop(key, None)

    respond(
        text=f"Approved: {namespace}/{workload}. {msg}",
        replace_original=True,
    )


@bolt_app.action("finops_deny")
def handle_deny(ack, body, respond):
    ack()
    key = body["actions"][0]["value"]
    cluster, namespace, workload = _parse_proposal_key(key)

    if not namespace or not workload:
        respond(text=f"Invalid proposal key: {key}", replace_original=True)
        return

    log.info("Slack deny clicked: %s", key)
    intent = pending_intents.get(key, {})
    ticket_key = intent.get("jira_ticket", "")
    _comment_jira_ticket(ticket_key, f"Denied via Slack. Workload suppressed for 7 days.")
    _close_jira_ticket(ticket_key)

    msg = _handle_deny(cluster, namespace, workload)
    pending_intents.pop(key, None)

    respond(text=msg, replace_original=True)


