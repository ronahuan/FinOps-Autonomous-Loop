"""Approval server — bridges Slack Approve/Deny buttons to AAP workflow approvals.

The AAP workflow handles notification (notify-slack.yml with buttons) and
remediation. This server only handles Slack button clicks and bridges them
to AAP's workflow approval API.

Endpoints:
  POST /intent        — receives proposed intents from the Observer, forwards to AAP Event Stream
  POST /slack/events  — Slack Bolt handles interactive button clicks
  GET  /health        — readiness probe

When AAP is configured (AAP_API_URL set):
  Approve → finds pending AAP approval node, calls approve API
  Deny    → finds pending AAP approval node, calls deny API + suppresses workload

When AAP is NOT configured (standalone ROSA fallback):
  Approve → creates Actor K8s Job directly
  Deny    → suppresses workload via K8s annotation
"""
from __future__ import annotations

import json
import logging
import os
import re
from datetime import datetime, timezone, timedelta

import httpx
from dotenv import load_dotenv
from flask import Flask, request, jsonify
from slack_bolt import App
from slack_bolt.adapter.flask import SlackRequestHandler

load_dotenv()

logging.basicConfig(level=logging.INFO, format="[approval] %(message)s")
log = logging.getLogger("approval")

SLACK_BOT_TOKEN = os.environ.get("SLACK_BOT_TOKEN", "")
SLACK_SIGNING_SECRET = os.environ.get("SLACK_SIGNING_SECRET", "")
SLACK_WEBHOOK_URL = os.environ.get("SLACK_WEBHOOK_URL", "")
AAP_API_URL = os.environ.get("AAP_API_URL", "")
AAP_API_TOKEN = os.environ.get("AAP_API_TOKEN", "")
AAP_EVENT_STREAM_URL = os.environ.get("AAP_EVENT_STREAM_URL", "")
AAP_EVENT_STREAM_USER = os.environ.get("EDA_WEBHOOK_USER", "")
AAP_EVENT_STREAM_PASS = os.environ.get("EDA_WEBHOOK_PASSWORD", "")
ACTOR_IMAGE = os.environ.get("ACTOR_IMAGE", "quay.io/pm_ronahuan/finops-actor:latest")
ACTOR_JOB_NAMESPACE = os.environ.get("ACTOR_JOB_NAMESPACE", "finops-demo")
JIRA_URL = os.environ.get("JIRA_URL", "")
JIRA_USER = os.environ.get("JIRA_USER", "")
JIRA_API_TOKEN = os.environ.get("JIRA_API_TOKEN", "")
JIRA_PROJECT_KEY = os.environ.get("JIRA_PROJECT_KEY", "")

pending_intents: dict[str, dict] = {}
processed: set[str] = set()

if SLACK_BOT_TOKEN and SLACK_SIGNING_SECRET:
    bolt_app = App(token=SLACK_BOT_TOKEN, signing_secret=SLACK_SIGNING_SECRET)
else:
    bolt_app = App(token="xoxb-placeholder", signing_secret="placeholder",
                   token_verification_enabled=False)
    log.warning("SLACK_BOT_TOKEN/SLACK_SIGNING_SECRET not set — Slack button verification disabled")

flask_app = Flask(__name__)
bolt_handler = SlackRequestHandler(bolt_app)


def _proposal_key(intent: dict) -> str:
    return f"{intent.get('cluster', '')}__{intent.get('namespace', '')}__{intent.get('workload', '')}"


def _parse_proposal_key(key: str) -> tuple[str, str, str]:
    parts = key.split("__", 2)
    if len(parts) == 3:
        return parts[0], parts[1], parts[2]
    return "", "", ""


def _sanitize_env_name(cluster: str) -> str:
    return re.sub(r"[^A-Z0-9]", "_", cluster.upper())


